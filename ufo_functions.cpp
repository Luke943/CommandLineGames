void greet()
{
    std::cout << "=============\n"
              << "UFO: The Game\n"
              << "=============\n"
              << "Instructions: save your friend from alien abduction by guessing the letters in the codeword.\n";
}

void end_game(std::string answer, std::string codeword)
{
    std::cout << "The codeword was " << codeword << ".\n";
    if (answer == codeword)
    {
        std::cout << "Hooray! You saved the person and earned a medal of honor!\n";
    }
    else
    {
        std::cout << "Oh no! The UFO just flew away with another person!\n";
    }
}

void display_status(std::vector<char> incorrect, std::string answer)
{
    std::cout << "Incorrect guesses: ";
    for (auto x : incorrect)
    {
        std::cout << x << " ";
    }
    std::cout << "\n";
    std::cout << "Codeword: ";
    for (auto x : answer)
    {
        std::cout << x << " ";
    }
    std::cout << "\n";
}

bool play_again()
{
    char input = 'x';
    std::cout << "Would you like to play again? (y/n) ";
    while (true)
    {
        std::cin >> input;
        input = tolower(input);
        if (input == 'y')
        {
            return true;
        }
        else if (input == 'n')
        {
            return false;
        }
        else
        {
            std::cout << "Invalid input. Enter 'y' or 'n': ";
        }
    }
}

std::vector<std::string> read_words(const std::string filename)
{
    std::ifstream file;
    std::string word{};
    std::vector<std::string> all_words{};

    file.open(filename);
    if (!file.is_open())
    {
        std::cout << "Failed to load words from file.\n";
    }

    while (file >> word)
    {
        all_words.push_back(word);
    }

    file.close();
    return all_words;
}

